function renderAzkar() {
    let list = document.getElementById('azkar-list');
    list.innerHTML = '';
    let remaining = azkar.length;
    azkar.forEach((zkr, idx) => {
        let card = document.createElement('div');
        card.className = 'azkar-card';
        card.innerHTML = `
            <div class="azkar-title">${zkr.title}</div>
            <div class="azkar-text">${zkr.text}</div>
            <div class="azkar-repeat">التكرار: ${zkr.repeat}</div>
            <div class="azkar-fadl"><span>فضل الذكر:</span> ${zkr.fadl}</div>
        `;
        card.style.cursor = 'pointer';
        card.title = 'اضغط لإخفاء الذكر';
        card.addEventListener('click', function() {
            card.style.transition = 'opacity 0.4s, height 0.4s';
            card.style.opacity = '0';
            card.style.height = '0';
            setTimeout(() => {
                card.remove();
                remaining--;
                if (remaining === 0) showFinishMessage();
            }, 400);
        });
        list.appendChild(card);
    });
}

function showFinishMessage() {
    let list = document.getElementById('azkar-list');
    let msg = document.createElement('div');
    msg.className = 'finish-message';
    msg.innerHTML = `
        <div style="text-align:center; padding:30px 10px;">
            <h2 style="color:#009688;">مبروك! لقد أنهيت جميع الأذكار 🎉</h2>
            <button id="read-again-btn" style="margin-top:18px;padding:10px 22px;font-size:1.1em;background:#009688;color:#fff;border:none;border-radius:8px;cursor:pointer;">اقرأ الأذكار مرة أخرى</button>
        </div>
    `;
    list.appendChild(msg);
    document.getElementById('read-again-btn').onclick = function() {
        window.location.reload()
    };
}

document.addEventListener('DOMContentLoaded', renderAzkar);
